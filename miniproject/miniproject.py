#!/usr/bin/env python3

grade = float(input("Enter your grade:"))

if grade >= 90:
    print("A, you're excellent job!")

elif grade >= 80:
    print("B, not bad you're smarter than average!")

elif grade >= 70:
    print("C, as Tony from the Sopranos would say 'You're average at best!'")

elif grade >= 60:
    print("D, jesus do better. God speed my friend!")

elif grade <= 59:
    print("F, get the f out")
